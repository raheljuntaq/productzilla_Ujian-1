// Harga
const hargaBaju = 75000;
const hargaCelana = 120000;
const hargaTas = 250000;

// Jumlah produk yang dibeli
const jmlBaju = 3;
const jmlCelana = 2;
const jmlTas = 1;

// Operator perhitungan
